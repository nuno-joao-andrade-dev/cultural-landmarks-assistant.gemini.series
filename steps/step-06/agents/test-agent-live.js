const { Runner, InMemorySessionService, stringifyContent } = require('@google/adk');
const { agent } = require('./run-agent');
const dotenv = require('dotenv');

dotenv.config();

async function testAgent() {
  console.log('--- Live Agent Test (Gemini 3.1 Flash-Lite) ---');
  console.log('Model:', agent.model);
  
  const runner = new Runner({
    appName: 'CulturalLandmarksWorkshop',
    agent,
    sessionService: new InMemorySessionService(),
  });

  const prompt = "Hello, introduce yourself as a Cultural Landmarks workshop assistant.";
  console.log(`\nUser: ${prompt}`);

  try {
    const events = runner.runEphemeral({
      userId: 'test-user',
      newMessage: { role: 'user', parts: [{ text: prompt }] },
    });
    
    let receivedAny = false;
    for await (const event of events) {
      if (event.author !== 'user') {
        const text = stringifyContent(event);
        if (text) {
          process.stdout.write(text);
          receivedAny = true;
        }
      }
    }
    
    if (!receivedAny) {
      console.log('\n[Warning] No text response was generated by the agent.');
    }
    
    console.log('\n--- End of Stream ---');

  } catch (error) {
    console.error(`\nError: ${error.message}`);
  }
}

testAgent().catch(console.error);
